// WHILE LOOPS

let i = 0;

while (i < 5) {
  console.log("loop: ", i);
  i++;
}

const names = ["Dug", "Binki", "Kanga"];

let j = 0;

while (j < names.length) {
  console.log(names[j]);
  j++;
}

// NOTES:
/*
- similar to FOR loops, it loops through a chunk of code a certain amount of times.
- syntax differences
- we set i outside of the code block
- the while only hold the condition
- increment is inside of the loop
*/
